def hitung_nilai_akhir(tugas, uts, uas):
    return (0.3 * tugas) + (0.3 * uts) + (0.4 * uas)

def tentukan_kategori(nilai_akhir):
    if nilai_akhir >= 85:
        return 'A', 'Sangat Baik'
    elif nilai_akhir >= 75:
        return 'B', 'Baik'
    elif nilai_akhir >= 65:
        return 'C', 'Cukup'
    elif nilai_akhir >= 50:
        return 'D', 'Kurang'
    else:
        return 'E', 'Sangat Kurang'

def keterangan_lulus(nilai_huruf):
    return "LULUS" if nilai_huruf in ['A', 'B', 'C'] else "TIDAK LULUS"

def main():
    print("=== Program Penilaian Mahasiswa ===")
    nama = input("Nama Mahasiswa: ")

    try:
        tugas = float(input("Nilai Tugas (0-100): "))
        uts = float(input("Nilai UTS (0-100): "))
        uas = float(input("Nilai UAS (0-100): "))

        nilai_akhir = hitung_nilai_akhir(tugas, uts, uas)
        nilai_huruf, deskripsi = tentukan_kategori(nilai_akhir)
        status = keterangan_lulus(nilai_huruf)

        print("\n--- Hasil Penilaian ---")
        print(f"Nama         : {nama}")
        print(f"Nilai Akhir  : {nilai_akhir:.2f}")
        print(f"Nilai Huruf  : {nilai_huruf}")
        print(f"Deskripsi    : {deskripsi}")
        print(f"Keterangan   : {status}")

    except ValueError:
        print("Input harus berupa angka!")

if __name__ == "__main__":
    main()

