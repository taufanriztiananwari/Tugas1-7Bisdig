akun = {
    "pin": "1234",
    "saldo": 10000000000  # dalam Rupiah
}

def verifikasi_pin():
    for i in range(3):
        pin_input = input("Masukkan PIN: ")
        if pin_input == akun["pin"]:
            return True
        else:
            print("PIN salah.")
    print("3x percobaan gagal. Kartu diblokir.")
    return False

def cek_saldo():
    print(f"Saldo Anda saat ini: ${akun['saldo']:,}")

def tarik_tunai():
    jumlah = int(input("Masukkan jumlah yang ingin ditarik: $"))
    if jumlah <= 0:
        print("Jumlah tidak valid.")
    elif jumlah > akun["saldo"]:
        print("Saldo tidak mencukupi.")
    else:
        akun["saldo"] -= jumlah
        print(f"Berhasil menarik ${jumlah:,}. Saldo tersisa: ${akun['saldo']:,}")

def setor_tunai():
    jumlah = int(input("Masukkan jumlah yang ingin disetor: $"))
    if jumlah <= 0:
        print("Jumlah tidak valid.")
    else:
        akun["saldo"] += jumlah
        print(f"Berhasil menyetor ${jumlah:,}. Saldo sekarang: ${akun['saldo']:,}")

def transfer():
    rekening = input("Masukkan nomor rekening tujuan: ")
    jumlah = int(input("Masukkan jumlah yang ingin ditransfer: $"))
    if jumlah <= 0:
        print("Jumlah tidak valid.")
    elif jumlah > akun["saldo"]:
        print("Saldo tidak mencukupi untuk transfer.")
    else:
        akun["saldo"] -= jumlah
        print(f"Transfer ke {rekening} sebesar ${jumlah:,} berhasil. Sisa saldo: ${akun['saldo']:,}")

def ganti_pin():
    pin_lama = input("Masukkan PIN lama: ")
    if pin_lama != akun["pin"]:
        print("PIN lama salah.")
        return
    pin_baru = input("Masukkan PIN baru (4 digit): ")
    if len(pin_baru) != 4 or not pin_baru.isdigit():
        print("PIN baru harus terdiri dari 4 digit angka.")
        return
    akun["pin"] = pin_baru
    print("PIN berhasil diubah.")

