import math

def input_angka(prompt):
    while True:
        try:
            return float(input(prompt))
        except ValueError:
            print("Masukkan angka yang valid.")

def tambah(a, b):
    return a + b

def kurang(a, b):
    return a - b

def kali(a, b):
    return a * b

def bagi(a, b):
    if b == 0:
        return "Error: Pembagian oleh nol"
    else:
        return a / b

def pangkat(base, eksponen):
    return base ** eksponen

def akar_kuadrat(n):
    if n < 0:
        return "Error: Bilangan negatif tidak memiliki akar real"
    else:
        return math.sqrt(n)

def celsius_ke_fahrenheit(c):
    return (c * 9/5) + 32

def fahrenheit_ke_celsius(f):
    return (f - 32) * 5/9

def celsius_ke_kelvin(c):
    return c + 273.15

def kelvin_ke_celsius(k):
    return k - 273.15

def fahrenheit_ke_kelvin(f):
    c = fahrenheit_ke_celsius(f)
    return celsius_ke_kelvin(c)

def kelvin_ke_fahrenheit(k):
    c = kelvin_ke_celsius(k)
    return celsius_ke_fahrenheit(c)

